package by.bsuir.kaziukovich.oop.logic.command;

/**
 * Static constructor containing constants for commands response
 */
public class CommandResponse {
    /**
     * Constant string to describe command execution success response
     */
    public final static String SUCCESS_RESPONSE = "SUCCESS";

    /**
     * Constant string to describe command execution failure response
     */
    public final static String FAILURE_RESPONSE = "FAILURE";

    /**
     * Private constructor to avoid object creation
     */
    private CommandResponse() { }
}
